# Escape Sequence Characters
#\b => Back Space   \b
print("Hello\bWorld")  #will Remove o

# Escape newLines  \newLine
print("hello \
  i LOVE \
  PYTHON")    

# Escape Back Slash \\
print("I love Back Slash \\  ")


# Escape Single Quote  \'
print('I love Back Slash \'test\' ')

#Escape Double Quote  \""
print("I love Back Slash \"test\" ")

#Line Feed \n
print("hello world\nsecond")

#Carriage Return  \r
print("12333333\rk")

#Horizontal Tab   \t
print("hello\tworld")

#Character hexaValue \xhh
print("\x73")