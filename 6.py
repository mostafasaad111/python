# Data type

print(type(10))  # int

print(type(10.5))  # float

print(type("Mustafa")) #string
print(type("Hello python")) #string

print(type([1,2,3,4,5,6]))  #list 

print(type((1,2,3,4,5,6))) #tuple

print(type({"one":1, "two":2})) #dictionary

print(type(2 == 2)) #boolean

